# Maak een lijst met de gasten
gasten = ["Jij", "Paul", "Kees", "Marie", "Hilda"]

# Print de lijst met gasten
print("De gastenlijst is:", gasten)

# Marie belt af, verwijder haar uit de lijst
gasten.remove("Marie")

# Print de gewijzigde lijst
print("Marie belt af, de nieuwe lijst is:", gasten)

# George wil aanschuiven, voeg hem toe
gasten.insert(2, "George")

# Print de lijst met George
print("George wil ook mee, de nieuwe lijst is:", gasten)
